package March_19th.Test3;

public class Student {
    private int num;
    private int score;

    public Student(int num, int score) {
        this.num = num;
        this.score = score;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
